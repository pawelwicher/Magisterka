#include "Chromosom.h"
/*******************************************************************************
******************************** KONSTRUKTOR ***********************************
*******************************************************************************/

Chromosom::Chromosom()
{N=LICZBA_MIAST; ch=new int[N]; for(int i=0;i<N;i++) ch[i]=0;}

/*******************************************************************************
********************************** DESTRUKTOR **********************************
*******************************************************************************/

Chromosom::~Chromosom()
{delete []ch;}

/*******************************************************************************
********************************* POLICZ DROGE *********************************
*******************************************************************************/

float Chromosom::PoliczDroge()
{
float droga=0;
   
    for(int i=0;i<N-1;i++)
        droga+=Trasy[ch[i]-1][ch[i+1]-1];
        
    droga+=Trasy[ch[0]-1][ch[N-1]-1];
return droga;
}

